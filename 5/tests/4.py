data = {"Мичурин А. К.": "20.05.2006",
    "Якупов О. Л.": "20.05.2007",
    "Елизарова А. К.": "19.04.2007",
    "Петраев С. С.": "01.01.2006",
    "Метов К. О.": "31.12.2005",
    "Горшков П. П.": "30.11.2006"
}
